public class SearchitemAdapter extends BaseQuickAdapter<NetVideoBean,BaseViewHolder> {
    private Context context;


    public SearchitemAdapter(Context context) {
        super(R.layout.item_search);
        this.context=context;
    }

    @Override
    protected void convert(BaseViewHolder helper, NetVideoBean item) {
        helper.setText(R.id.tv_title,item.getTitle())
                .setText(R.id.tv_type,item.getType())
                .setText(R.id.tv_date,item.getTime());

    }
}
